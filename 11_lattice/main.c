unsigned char animation[] = {
	0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,
	0xFF,0x08,0x08,0x08,0xFF,0x08,0x10,0x20,
	0x08,0x10,0x20,0x08,0x10,0x20,0x08,0x10,
	0x20,0x08,0x10,0x20,0x08,0x10,0x20,0x08,
	0x10,0x20,0xFD,0x00,0xFD,0x00,0x00,0x00,
	0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,
};

void main()
{
	unsigned char i = 0;
	unsigned char offset = 0;
	unsigned char count = 0;
	MatrixLED_Init();
	while(1)
	{
		for(i = 0; i < 8; ++i)
		{
			MatrixLED_ShowColumn(i, animation[i + offset]);
		}
		count++;
		if(count > 5)
		{
			count = 0;
			offset++;
			if(offset>40)
			{
				offset = 0;
			}
		}
	}
}
